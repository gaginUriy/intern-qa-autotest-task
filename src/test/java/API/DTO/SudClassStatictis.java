
public class SudClassStatictis {

        private int likes;
        private int viewCount;
        private int contacts;


        public SudClassStatictis(int likes, int viewCount, int contacts) {
            this.likes = likes;
            this.viewCount = viewCount;
            this.contacts = contacts;
        }


        public int getLikes() {
            return likes;
        }

        public int getViewCount() {
            return viewCount;
        }

        public int getContacts() {
            return contacts;
        }
    }

